static const uint8_t *_ch_data_20x40(char ch)
{
    int offset = 0;
    const uint8_t *p = _ascii_20x40;
    if ((ch >= '0') && (ch <= '9')) {
        ch -= '0';
        offset = ch * _FONT_20X40_DATA_STEP;
    } else if (ch == ':') {
        offset = 10 * _FONT_20X40_DATA_STEP;
    } else {
        offset = 11 * _FONT_20X40_DATA_STEP;
    }

    return p + offset;
}

