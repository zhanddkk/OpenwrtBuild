static const uint8_t *_ch_data_12x24(char ch)
{
    int offset = 0;
    const uint8_t *p = _ascii_12x24;
    if ((ch >= '0') && (ch <= '9')) {
        ch -= '0';
        offset = ch * _FONT_12X24_DATA_STEP;
    } else {
        switch (ch)
        {
        case ':':
            offset = 10 * _FONT_12X24_DATA_STEP;
            break;
        case '-':
            offset = 12 * _FONT_12X24_DATA_STEP;
            break;
        case '.':
            offset = 13 * _FONT_12X24_DATA_STEP;
            break;
        case '%':
            offset = 14 * _FONT_12X24_DATA_STEP;
            break;
        default: /* ' ' */
            offset = 11 * _FONT_12X24_DATA_STEP;
            break;
        }
    }

    return p + offset;
}

const struct font font_12x24 = {
    12, 24, _ch_data_12x24
};
